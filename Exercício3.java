public class Exercício3 {
    public static void main(String[] args) {
        // Loop de 1 até 100
        for (int i = 1; i <= 100; i++) {
            // Verificar se o número é múltiplo de 3
            if (i % 3 == 0) {
                System.out.println(i);
            }
        }
    }
}
